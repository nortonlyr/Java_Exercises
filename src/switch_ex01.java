
public class switch_ex01 {
	public static void main(String[] args) {
	int grade = 20;
	switch(grade) {
	case 10: System.out.println("Grade A"); break;
	case 9: System.out.println("Grade A"); break;
	case 8: System.out.println("Grade B"); break;
	case 7: System.out.println("Grade C"); break;
	case 6: System.out.println("Grade D"); break;
	case 5: System.out.println("Grade F"); break;
	case 4: System.out.println("Grade F"); break;
	case 3: System.out.println("Grade F"); break;
	case 2: System.out.println("Grade F"); break;
	case 1: System.out.println("Grade F"); break;
	case 0: System.out.println("Grade F"); break;
	default: System.out.println("Invilid Grade");
	}
	}
}
